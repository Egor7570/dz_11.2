def upper_string(string):
    """
    Возвращает слово с заглавными буквами
    :param string: строка
    :return: строка большими буквами
    """
    return string.upper()


def title_string(string):
    """
    Возвращает первую букву заглавной
    :param string: строка
    :return:  Заглавная первая буква
    """
    return string.title()



